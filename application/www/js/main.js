var price = $('.price');
var finalPrice = 0;
for (let x of price)
{
  finalPrice += parseFloat(x.innerHTML);
}

$('.subTotal').text(finalPrice);
$('.subTotalTva').text((finalPrice*0.2).toFixed(2));
$('.total').text((finalPrice*1.2).toFixed(2));

